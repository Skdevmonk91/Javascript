//JavaScript While Loop
//while loop also implements like other loop with different structure
// While loop runs first condition and then execute code until condition is true 



//Creating a variable for while loop
let n = prompt("Enter your value to print: ");


//Making them to output convert into integer or only take integer
n = Number.parseInt(n);


//variable for to make condition variable
let i = 0;


//final while loop to execute and print integer
while (i < n) {
    console.log(i);
    i++;
}